namespace Neovolve.Switch.Skinning
{
    using System;

    /// <summary>
    /// The <see cref="SkinKey"/>
    ///   class is used to define the skin key names available in the skin configuration file.
    /// </summary>
    internal static class SkinKey
    {
        /// <summary>
        /// Defines the key name for the author.
        /// </summary>
        public const String Author = "Author";

        /// <summary>
        /// Defines the key name for the background colour.
        /// </summary>
        public const String Background = "BackColor";

        /// <summary>
        /// Defines the key name for the comments.
        /// </summary>
        public const String Comments = "Comments";

        /// <summary>
        /// Defines the key name for the cursor.
        /// </summary>
        public const String Cursor = "Cursor";

        /// <summary>
        /// Defines the key name for the email.
        /// </summary>
        public const String Email = "Email";

        /// <summary>
        /// Defines the key name for the font bold.
        /// </summary>
        public const String FontBold = "FontBold";

        /// <summary>
        /// Defines the key name for the font.
        /// </summary>
        public const String FontFamily = "Font";

        /// <summary>
        /// Defines the key name for the font italic.
        /// </summary>
        public const String FontItalic = "FontItalic";

        /// <summary>
        /// Defines the key name for the font size.
        /// </summary>
        public const String FontSize = "FontSize";

        /// <summary>
        /// Defines the key name for the foreground colour.
        /// </summary>
        public const String Foreground = "ForeColor";

        /// <summary>
        /// Defines the key name for the height coordinate.
        /// </summary>
        public const String Height = "Height";

        /// <summary>
        /// Defines the key name for the image.
        /// </summary>
        public const String Image = "Image";

        /// <summary>
        /// Defines the key name for the left coordinate.
        /// </summary>
        public const String Left = "Left";

        /// <summary>
        /// Defines the key name for the name.
        /// </summary>
        public const String Name = "Name";

        /// <summary>
        /// Defines the key name for the screenshot.
        /// </summary>
        public const String Screenshot = "Screenshot";

        /// <summary>
        /// Defines the key name for the top coordinate.
        /// </summary>
        public const String Top = "Top";

        /// <summary>
        /// Defines the key name for the transparent color.
        /// </summary>
        public const String TransparentColor = "TransparentColor";

        /// <summary>
        /// Defines the key name for the url.
        /// </summary>
        public const String Url = "URL";

        /// <summary>
        /// Defines the key name for the version.
        /// </summary>
        public const String Version = "Version";

        /// <summary>
        /// Defines the key name for the width coordinate.
        /// </summary>
        public const String Width = "Width";
    }
}