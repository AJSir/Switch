namespace Neovolve.Switch.Skinning
{
    using System;

    /// <summary>
    /// The <see cref="SkinAuthor"/>
    ///   class is used to describe the author of a skin.
    /// </summary>
    public class SkinAuthor
    {
        /// <summary>
        ///   Gets or sets the email.
        /// </summary>
        /// <value>
        ///   The email.
        /// </value>
        public String Email
        {
            get;
            set;
        }

        /// <summary>
        ///   Gets or sets the name.
        /// </summary>
        /// <value>
        ///   The name.
        /// </value>
        public String Name
        {
            get;
            set;
        }

        /// <summary>
        ///   Gets or sets the website.
        /// </summary>
        /// <value>
        ///   The website.
        /// </value>
        public String Website
        {
            get;
            set;
        }
    }
}